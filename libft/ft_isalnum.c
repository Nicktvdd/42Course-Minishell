#include "libft.h"

int	ft_isalnum(int car)
{
	if (car >= 48 && car <= 57)
		return (1);
	else if (car >= 65 && car <= 90)
		return (1);
	else if (car >= 97 && car <= 122)
		return (1);
	else
		return (0);
}
